Map::Map(){
    brown = ImageHandler::Load("brown.png");
    blue = ImageHandler::Load("blue.png");
    red = ImageHandler::Load("red.png");

   for(int i = 0; i < 32; i++){
       for(int j = 0; j< 32; j++){
           tileSet[i][j] = map[i][j];
       }
   }

    src.x = 0;
    src.y = 0;
    src.w = 32;
    src.h = 32;
    
    dest.w = 32;
    dest.h = 32;
    dest.x = 0;
    dest.y = 0;
}





void Map::DrawMap(){

    int type = 0;
    for(int row = 0; row < 32; row++){

        for(int col = 0; col < 32; col++){
            
            type = tileSet[row][col];

            
            dest.x = col * 32;
            dest.y = row * 32;

            if(type == 0){
                ImageHandler::Create(blue, src, dest);
            }
            if(type == 1){
                ImageHandler::Create(brown, src, dest);
            }
            if(type == 2){
                ImageHandler::Create(red, src, dest);
            }
        }
    }

}